    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (tblProducts.getSelectedRowCount() == 1) {
            int numRow = tblProducts.getSelectedRow();
            int answer = JOptionPane.showConfirmDialog(null, "Desea eliminar el producto");
            System.out.println(numRow);
            if (answer == 0) { //Si su respuesta es si
                if (dataProduct.remove(numRow) != null) {
                    showTable();
                } else {
                    JOptionPane.showMessageDialog(null, "Error al borrar");
                }
            }
        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar 1 sólo registro");
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

