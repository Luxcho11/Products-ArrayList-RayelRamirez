    private void txtSearchKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtSearchKeyReleased
        String titulos[] = {"Id", "Descripción", "Categoría",
            "Precio", "Stock", "Mínimo", "Fecha Registro"};
        model = new DefaultTableModel(null, titulos);

        for (int i = 0; i < dataProduct.size(); i++) {
            product = dataProduct.get(i);
            if (product.getDescription().toLowerCase().contains(txtSearch.getText().toLowerCase())) {

                Object nuevaFila[] = {product.getId(), product.getDescription(), product.getCategory(),
                    product.getPrice(), product.getUnitsInStock(), product.getMinimum(),
                    product.getResgistrationDate()};
                model.addRow(nuevaFila);
            }
        }
        tblProducts.setModel(model);

        lblRegisters.setText("Cantidad de registros: " + String.valueOf(model.getRowCount()));
    }//GEN-LAST:event_txtSearchKeyReleased

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
        if (tblProducts.getSelectedRowCount() == 1) {
            int posi = tblProducts.getSelectedRow();
            DlgNewProduct winNew = new DlgNewProduct(new FrmProductManager(), true, dataProduct, posi);
            winNew.setTitle("Editar Producto");
            winNew.setLocationRelativeTo(null);
            winNew.setVisible(true);
            this.dataProduct = winNew.dataProduct;

            showTable();

        } else {
            JOptionPane.showMessageDialog(null, "Debe seleccionar 1 sólo registro");
        }
    }//GEN-LAST:event_btnEditActionPerformed

    private void showTable() {

        String titulos[] = {"Id", "Descripción", "Categoría",
            "Precio", "Stock", "Mínimo", "Fecha Registro"};
        model = new DefaultTableModel(null, titulos);

        for (int i = 0; i < dataProduct.size(); i++) {
            product = dataProduct.get(i);
            Object nuevaFila[] = {product.getId(), product.getDescription(), product.getCategory(),
                    product.getPrice(), product.getUnitsInStock(), product.getMinimum(),
                    product.getResgistrationDate()};
            model.addRow(nuevaFila);

        }
        tblProducts.setModel(model);

        lblRegisters.setText("Cantidad de registros: " + String.valueOf(model.getRowCount()));
    }

